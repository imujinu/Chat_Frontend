<template>
  <v-container>
    <v-row justify="center">
      <v-col cols="12" sm="4" md="6">
        <v-card>
          <v-card-title class="text-h5 text-center">회원가입</v-card-title>
        </v-card>
        <v-card-text>
          <v-form @submit.prevent="memberCreate">
            <v-text-field label="이름" v-model="name" required> </v-text-field>
            <v-text-field
              label="비밀번호"
              v-model="password"
              required
              type="password"
            >
            </v-text-field>
            <v-text-field label="이메일" v-model="email" required type="email">
            </v-text-field>
            <v-btn type="submit" color="primary" block>등록 </v-btn>
          </v-form>
        </v-card-text>
      </v-col>
    </v-row>
  </v-container>
</template>
<script>
import axios from "axios";
export default {
  data() {
    return {
      name: "",
      email: "",
      password: "",
    };
  },
  methods: {
    async memberCreate() {
      const data = {
        name,
        email,
        password,
      };
      const result = await axios.post(
        "http://localhost:8080/member/create",
        data
      );
      this.$router.push("/");
    },
  },
};
</script>
